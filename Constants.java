/** Constants for Tic-Tac-Toe representing the different marks that can be made on the board */
public interface Constants {
    /** represents an unmarked square */
    static final char SPACE_CHAR = ' ';
    /** represents a square marked by the 'O' player*/
    static final char LETTER_O = 'O';
    /** represents a square marked by the 'X' player */
    static final char LETTER_X = 'X';
}
